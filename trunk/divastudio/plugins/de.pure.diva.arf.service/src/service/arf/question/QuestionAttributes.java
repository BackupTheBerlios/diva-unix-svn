package service.arf.question;

/**
 * The question attributes characterises a question.
 */
public class QuestionAttributes {
  /**
   * Question implicates optimising.
   */
  public Boolean OPTIMISING = Boolean.FALSE;
  /**
   * Question implicates solving.
   */
  public Boolean SOLVING    = Boolean.FALSE;
  /**
   * Question implicates validating.
   */
  public Boolean VALIDATING = Boolean.FALSE;
}
